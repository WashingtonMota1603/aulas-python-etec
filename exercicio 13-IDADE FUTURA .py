# 13-idade-futura.py

# Solicita a idade atual e converte para número inteiro
idade_atual = int(input("Qual é a sua idade atual? "))

# Calcula a idade em 2035 (2035 - 2026 = 9 anos de diferença)
idade_futura = idade_atual + 9

# Exibe o resultado
print(f"Em 2035, você terá {idade_futura} anos.")



